class MissingPerson {
  final String id;
  final String name;
  final String birthday;
  final String birthplace;
  final String place;
  final String details;
  final String avatarUrl;

  const MissingPerson({
    required this.id,
    required this.name,
    required this.birthday,
    required this.birthplace,
    required this.details,
    required this.place,
    required this.avatarUrl,
  });
}
